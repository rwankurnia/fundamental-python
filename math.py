import math

print(abs(-13))

print(math.fabs(-4)) # menghilangkan nilai negatif menjadi positif

print(math.pow(8, 2)) # menghitung pangkat, 8 pangkat 2

print(math.sqrt(64)) #mencari akarnya

print(round(4.7)) #pembulatan normal matematika

print(round(4.4)) #pembulatan normal matematika

print(math.floor(4.7)) #pembulatan tetap kebawah

print(math.ceil(4.4)) #pembulatan tetap ke atas


